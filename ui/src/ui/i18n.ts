// Helper function to get translated text
export function t(lang: Lang, section: keyof typeof i18n, key: string): string {
  const sectionData = i18n[section];
  if (!sectionData) return key;
  const langData = sectionData[lang] as Record<string, string>;
  if (!langData) return key;
  return langData[key] || key;
}

// Get entire section translations
export function getSection<T extends keyof typeof i18n>(lang: Lang, section: T): typeof i18n[T]["zh"] {
  return i18n[section][lang] as typeof i18n[T]["zh"];
}
